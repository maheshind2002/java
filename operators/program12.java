package operators;

class Switchdemo1{
	public static void main(String [] args){
		char ch  = 'C';

		switch(ch){
			case 'A':
				System.out.println("Captial A");
				break;
			case 'a':
				System.out.println("small a");
				break;
			case 'B':
				System.out.println("Captial B");
				break;
			case 'C':
				System.out.println("Captial C");
				break;
			case 'c':
				System.out.println("small c");
				break;
			default :
				System.out.println("Default case");

				
				
		}
	}
}

