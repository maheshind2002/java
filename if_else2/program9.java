package if_else2;
// based on values calculating the given triangle is pythagorus or not
class Pro9{
	public static void main (String[] args){
		int a=5;
		int b=3;
		int c=8;
		if(a*a+b*b==c*c){
			System.out.println("it is a pythagorus theorem");
		}
		else{
			System.out.println("it is not a pythagorus theorem");
		}
	}
}
