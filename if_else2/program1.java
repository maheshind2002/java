package if_else2;
// ckecking the number is between  1 to 1000 or not
class Number{
	public static void main(String[] args){
		int num = 968;

		if (num>=0 && num<=1000){
			System.out.println(num +" given number is in 1 to 1000");
		}
		else{
			System.out.println("number not in range");
		}
	}
}

